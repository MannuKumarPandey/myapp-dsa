package viveksingh;

public class OnOffTest {

    public static void main(String[] args) {


        //case 1
        boolean flag = true;
        if(flag){
            System.out.println("System is showing as on now.");
        }else{
            System.out.println("System is showing as off now.");
        }


        //case 2
        boolean flagNext = false;
        if(flagNext){
            System.out.println("System is showing as on now.");
        }else{
            System.out.println("System is showing as off now.");
        }
    }
}
